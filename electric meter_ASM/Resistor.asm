;=======================================================================
ADC_Init_OHM:
;=======================================================================
	;---------------------------ADCNET--------------------------------
	CALL		50KOHM
	;---------------------------ADCNETSET----------------------------
	CALL		500to50KOHMADCSET	
	;----------------------------------------------------------------------
	CALL		ADC_F
  	RET
  	
50AND500OHM:
	MVL		00000110B
	MVF		PA10,F,ACCE
	MVL		0H
	MVF		PA54,F,ACCE
	MVF		PA32,F,ACCE
	MVF		PAX6,F,ACCE
	RET	
5KOHM:
	MVL		01100000B
	MVF		PA10,F,ACCE
	MVL		0H
	MVF		PA54,F,ACCE
	MVF		PA32,F,ACCE
	MVF		PAX6,F,ACCE
	RET		
50KOHM:
	MVL		01100000B
	MVF		PA32,F,ACCE
	MVL		0H
	MVF		PA54,F,ACCE
	MVF		PA10,F,ACCE
	MVF		PAX6,F,ACCE
	RET		
50MOHM:
	MVL		10001000B
	MVF		PA54,F,ACCE
	MVL		00000000B
	MVF		PA32,F,ACCE
	MVL		0H
	MVF		PA10,F,ACCE
	MVF		PAX6,F,ACCE
	RET	
	
50OHMADCSET:
	MVL 00000011B 
  	MVF ADCN1,F,ACCE
  	MVL 00010100B;SMOD
  	MVF ADCN2,F,ACCE
  	MVL 00000000B ;
  	MVF ADCN3,F,ACCE
  	MVL 00011111B;AD1OSR=010:32 OSR
  	MVF ADCN4,F,ACCE
  	MVL 10000101B ;PB1 pb3
  	MVF ADCN5,F,ACCE
  	MVL 00000000B 
  	MVF ADCN6,F,ACCE
  	MVL 11110000B
  	MVF ADCN7,F,ACCE
  	ret		
50TO500OHMADCSET:
	MVL 00000011B 
  	MVF ADCN1,F,ACCE
  	MVL 00010100B;SMOD
  	MVF ADCN2,F,ACCE
  	MVL 00000000B ;
  	MVF ADCN3,F,ACCE
  	MVL 00011111B;AD1OSR=010:32 OSR
  	MVF ADCN4,F,ACCE
  	MVL 10000101B ;PB1 pb3
  	MVF ADCN5,F,ACCE
  	MVL 00000000B 
  	MVF ADCN6,F,ACCE
  	MVL 11110000B
  	MVF ADCN7,F,ACCE
  	ret	
500to50KOHMADCSET:
	MVL		01100000B
	MVF		PA32,F,ACCE
	MVL		0H
	MVF		PA54,F,ACCE
	MVF		PA10,F,ACCE
	MVF		PAX6,F,ACCE
	
	MVL 00000001B ;SFT1=00:10：前置Pre-filter 通道0Ω。
  	MVF ADCN1,F,ACCE
  	MVL 00010100B;SMOD
  	MVF ADCN2,F,ACCE
  	MVL 00000000B ;
  	MVF ADCN3,F,ACCE
  	MVL 00011111B;AD1OSR=010:32 OSR
  	MVF ADCN4,F,ACCE
  	MVL 10000101B ;PB1 pb3
  	MVF ADCN5,F,ACCE
  	MVL 00000000B 
  	MVF ADCN6,F,ACCE
  	MVL 10000000B 
;  	MVL 11110000B                
  	MVF ADCN7,F,ACCE
  	ret
500Kto50MOHMADCSET:
	MVL		10001000B
	MVF		PA54,F,ACCE
	MVL		00000000B
	MVF		PA32,F,ACCE
	MVL		0H
	MVF		PA10,F,ACCE
	MVF		PAX6,F,ACCE
	MVL 00000001B ;SFT1=00:10：前置Pre-filter 通道0Ω。
  	MVF ADCN1,F,ACCE
  	MVL 00000110B;SMOD
  	MVF ADCN2,F,ACCE
  	MVL 00000000B ;
  	MVF ADCN3,F,ACCE
  	MVL 00011111B;AD1OSR=010:32 OSR
  	MVF ADCN4,F,ACCE
  	MVL 10000101B ;PB1 pb3
  	MVF ADCN5,F,ACCE
  	MVL 10110100B 
  	MVF ADCN6,F,ACCE
  	MVL 10010000B 
  	MVF ADCN7,F,ACCE
  	ret
  		  